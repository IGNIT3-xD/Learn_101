player = ['Neymar', 'Messi', 'Ronaldo', 'Pele', 'Richarlison']
print(player)

print("-------------------------------------")

# Remove
player.remove('Richarlison')
print(player)

print("-------------------------------------")

# Pop
player.pop(1)  # Using index to remove the item
print(player)

print("-------------------------------------")

# Del
fruits = ["apple", "orange", "banana", "cherry"]
del fruits[0]
print(fruits)

print("-------------------------------------")

# Clear
fruits.clear()  # It will clear all the item
print(fruits)
