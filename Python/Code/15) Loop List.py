# For Loop
player = ['Neymar', 'Messi', 'Ronaldo', 'Pele', 'Platini', 'Dunga']

for x in player:
    print(x)
for i in range(len(player)):
    print("Serial no.", i)

print("-------------------------------------")

# While Loop
k = 0
while k < 6:
    print(k)
    k = k + 1

j = 0
while j < len(player):
    print(player[j])
    j = j + 1
